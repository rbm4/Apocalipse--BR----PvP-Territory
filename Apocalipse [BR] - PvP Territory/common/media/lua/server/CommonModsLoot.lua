local function appendUnique(target, source, seen)
    if not target or not source then
        return target or {}
    end
    seen = seen or {}
    for _, item in ipairs(target) do
        seen[item] = true
    end
    for _, item in ipairs(source) do
        if item and not seen[item] then
            table.insert(target, item)
            seen[item] = true
        end
    end
    return target
end

function CommonModsLoot.get(modKey)
    return CommonModsLoot.mods[modKey] or {}
end

function CommonModsLoot.merge(...)
    local merged = {}
    local seen = {}
    for i = 1, select("#", ...) do
        local source = select(i, ...)
        if type(source) == "string" then
            source = CommonModsLoot.get(source)
        end
        appendUnique(merged, source, seen)
    end
    return merged
end

function CommonModsLoot.mergeAll()
    local merged = {}
    local seen = {}
    for _, modKey in ipairs(CommonModsLoot.modOrder) do
        appendUnique(merged, CommonModsLoot.get(modKey), seen)
    end
    return merged
end

function CommonModsLoot.defineZoneGroup(zoneName, ...)
    CommonModsLoot.zoneGroups[zoneName] = CommonModsLoot.merge(...)
    return CommonModsLoot.zoneGroups[zoneName]
end

function CommonModsLoot.appendTo(target, ...)
    if not target then
        return CommonModsLoot.merge(...)
    end
    local seen = {}
    for _, item in ipairs(target) do
        seen[item] = true
    end
    for i = 1, select("#", ...) do
        local source = select(i, ...)
        if type(source) == "string" then
            source = CommonModsLoot.get(source)
        end
        appendUnique(target, source, seen)
    end
    return target
end

function CommonModsLoot.appendZoneGroupTo(target, zoneName)
    return CommonModsLoot.appendTo(target, CommonModsLoot.zoneGroups[zoneName] or {})
end

function CommonModsLoot.mergeIntoZone(lootTableZones, zoneName, ...)
    if not lootTableZones or not zoneName then
        return nil
    end
    for _, zone in ipairs(lootTableZones) do
        if zone.name == zoneName then
            zone.loot = CommonModsLoot.appendTo(zone.loot or {}, ...)
            return zone.loot
        end
    end
    return nil
end

